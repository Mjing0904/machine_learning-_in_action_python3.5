# 3.2.2 构造注解树
# 确定X轴长度：知道叶节点数目  确定Y轴长度:知道树的深度
# 3-6 获取叶节点的数目和树的层数


def getNumLeafs(myTree):
    numLeafs = 0
    # 最外面一层是数据全集，一定没有叶节点。获得第一个分割特征
    firstStr = list(myTree.keys())[0]
    secondDict = myTree[firstStr]
    for key in secondDict.keys():
        if type(secondDict[key]).__name__ == 'dict':
            numLeafs += getNumLeafs(secondDict[key])
        else:
            numLeafs += 1
    return numLeafs


def getTreeDepth(myTree):
    maxDepth = 0
    firstStr = list(myTree.keys())[0]    # myTree.keys()[0] TypeError: 'dict_keys' object does not support indexing
    secondDict = myTree[firstStr]
    for key in secondDict.keys():
        if type(secondDict[key]).__name__ == 'dict':
            thisDepth = 1 + getTreeDepth(secondDict[key])
        else:
            thisDepth = 1
        if thisDepth > maxDepth:
            maxDepth = thisDepth
    return maxDepth


